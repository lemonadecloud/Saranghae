const VERTICAL_JUNG = new Set(['ㅏ','ㅐ','ㅑ','ㅒ','ㅓ','ㅔ','ㅕ','ㅖ','ㅣ','ㅘ','ㅙ','ㅚ','ㅝ','ㅞ','ㅟ'])

type P = [number, number]
type RawStroke = P[]

interface Box { x: number; y: number; w: number; h: number }

function mapPt([px, py]: P, box: Box): StrokePoint {
  return { x: box.x + px * box.w / 100, y: box.y + py * box.h / 100 }
}

function mapStroke(pts: P[], box: Box): ComposedStroke {
  return { points: pts.map(p => mapPt(p, box)) }
}

function circ(cx: number, cy: number, r: number): P[] {
  const pts: P[] = []
  for (let i = 0; i <= 20; i++) {
    const a = (i / 20) * Math.PI * 2 - Math.PI / 2
    pts.push([cx + r * Math.cos(a), cy + r * Math.sin(a)])
  }
  return pts
}

