interface JamoBoxes {
  cho: Box
  jung: Box
  jong?: Box
}

function getLayout(jung: string, hasJong: boolean, size: number): JamoBoxes {
  const s = size
  const isV = VERTICAL_JUNG.has(jung)

  if (isV && !hasJong) return {
    cho:  { x: s*0.04, y: s*0.04, w: s*0.46, h: s*0.92 },
    jung: { x: s*0.50, y: s*0.04, w: s*0.46, h: s*0.92 },
  }
  if (!isV && !hasJong) return {
    cho:  { x: s*0.05, y: s*0.04, w: s*0.90, h: s*0.55 },
    jung: { x: s*0.05, y: s*0.59, w: s*0.90, h: s*0.37 },
  }
  if (isV && hasJong) return {
    cho:  { x: s*0.04, y: s*0.04, w: s*0.44, h: s*0.60 },
    jung: { x: s*0.48, y: s*0.04, w: s*0.48, h: s*0.60 },
    jong: { x: s*0.05, y: s*0.65, w: s*0.90, h: s*0.31 },
  }
  // horizontal + has jong
  return {
    cho:  { x: s*0.06, y: s*0.04, w: s*0.88, h: s*0.40 },
    jung: { x: s*0.06, y: s*0.44, w: s*0.88, h: s*0.25 },
    jong: { x: s*0.05, y: s*0.70, w: s*0.90, h: s*0.26 },
  }
}

export function getComposedStrokes(char: string, canvasSize: number): ComposedStroke[] {
  const code = char.charCodeAt(0) - 0xAC00
  if (code < 0 || code > 11171) return []

  const jongIdx = code % 28
  const jungIdx = Math.floor(code / 28) % 21
  const choIdx  = Math.floor(code / 28 / 21)

  const cho  = CHO[choIdx]
  const jung = JUNG[jungIdx]
  const jong = JONG[jongIdx]

  const layout = getLayout(jung, !!jong, canvasSize)
  const result: ComposedStroke[] = []

  const choRaw = JAMO[cho]
  if (choRaw) choRaw.forEach(pts => result.push(mapStroke(pts, layout.cho)))

  const jungRaw = JAMO[jung]
  if (jungRaw) jungRaw.forEach(pts => result.push(mapStroke(pts, layout.jung)))

  if (jong && layout.jong) {
    const jongBox = layout.jong
    const jongRaw = JAMO[jong]
    if (jongRaw) jongRaw.forEach(pts => result.push(mapStroke(pts, jongBox)))
  }

  return result
}
